def apply_colors(clubs):
    missing = []
    for club in clubs:
        name = club["name"]
        colors = CLUB_COLORS.get(name)
        if colors is None:
            missing.append(name)
            continue
        club["primary_color"], club["secondary_color"] = colors
    return missing


def main():
    for filename in ("clubs.json", "world_cup_teams.json"):
        path = ROOT / "data/club" / filename
        clubs = json.loads(path.read_text(encoding="utf-8"))
        missing = apply_colors(clubs)
        path.write_text(json.dumps(clubs, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")
        print(f"Updated {path.name} ({len(clubs)} records, {len(missing)} without colors)")
        if missing:
            raise SystemExit(f"Missing colors for: {', '.join(missing)}")


if __name__ == "__main__":
    main()
